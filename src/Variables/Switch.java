package Variables;

public class Switch {
	
	public static void main(String[] args) {
		
		
		
		byte num=5;
		
		switch (num) {
		
		case 127:
			System.out.println("It match at case1");
			
			
		case  -127:
			
			System.out.println("It match with case 2") ;
			
			
		case 124:
			System.out.println("It match in case 3") ;
			
		case 125:
			System.out.println("From case 4");
			
		
		
	}

}
}